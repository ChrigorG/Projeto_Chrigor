    private boolean prim = true;
    Conteudo4 meto = new Conteudo4();
    String erro = "91";
    private void JBproximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBproximoActionPerformed
        if(JRB1.isSelected() || JRB2.isSelected() || JRB3.isSelected() || JRB4.isSelected() || JRB5.isSelected()){
            if(prim && JRB1.isSelected()){
                meto.setPrimquest(1);
                prim = false;
                erro = "";
            } 
            
        if(JRB1.isSelected() && "1".equals(meto.getResult())){
            meto.setResult("correta");
        } else if(JRB2.isSelected() && "2".equals(meto.getResult())){
            meto.setResult("correta");
        } else if(JRB3.isSelected() && "3".equals(meto.getResult())){
            meto.setResult("correta");
        } else if(JRB4.isSelected() && "4".equals(meto.getResult())){
            meto.setResult("correta");
        } else if(JRB5.isSelected() && "5".equals(meto.getResult())){
            meto.setResult("correta");
        } else if(meto.getNumquestao() !=0){erro = erro + "," + String.valueOf(meto.getNumquestao());}
        Cont4DAO.Contador(meto);
        if(meto.getBotaoprox() == "Finalizar"){
            if(erro == "91"){
                erro = "Parabêns, Você acertou todas as questões!!!";
            }
            meto.setErros(erro);
            erro = "91";
            JOptionPane.showMessageDialog(null, "Parabêns você respondeu todas as questões do conteúdo 4, Clique em OK para ver seus resultados!");
            Barra_de_Progresso bar = new Barra_de_Progresso();
            bar.setVisible(true);
            this.dispose();
        }
        Cont4DAO.receberquestao(meto);
        calcule.setText(meto.getExercicio());
        JRB1.setText(meto.getAlt1());
        JRB2.setText(meto.getAlt2());
        JRB3.setText(meto.getAlt3());
        JRB4.setText(meto.getAlt4());
        JRB5.setText(meto.getAlt5());
        JBproximo.setText(meto.getBotaoprox());
        JLnum.setText(String.valueOf(meto.getNumquestao()));
        meto.setPrimquest(0);
        JRB1.setSelected(false);
        JRB2.setSelected(false);
        JRB3.setSelected(false);
        JRB4.setSelected(false);
        JRB5.setSelected(false);
        if (meto.getNumquestao() == 105) {
                ImageIcon img = new ImageIcon(getClass().getResource("/br/edu/ifms/tcc/imagens/Cont4_105.png"));
                JLimagem.setIcon(img);
        } else if (meto.getNumquestao() == 107) {
                ImageIcon img = new ImageIcon(getClass().getResource("/br/edu/ifms/tcc/imagens/Cont4_107.png"));
                JLimagem.setIcon(img);
        } else if (meto.getNumquestao() == 108) {
                ImageIcon img = new ImageIcon(getClass().getResource("/br/edu/ifms/tcc/imagens/Cont4_108.png"));
                JLimagem.setIcon(img);
        } else if (meto.getNumquestao() == 109) {
                ImageIcon img = new ImageIcon(getClass().getResource("/br/edu/ifms/tcc/imagens/Cont4_109.png"));
                JLimagem.setIcon(img);
        } else { ImageIcon img = new ImageIcon(getClass().getResource(""));
            JLimagem.setIcon(img);}
        } else {JOptionPane.showMessageDialog(null, "Selecione uma Alternativa");}
    }//GEN-LAST:event_JBproximoActionPerformed

