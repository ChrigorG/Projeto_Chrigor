    private void JBLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBLogarActionPerformed
        String nome_aluno = JTFLogin.getText();
        Usuario usuario = UsuarioDAO.verificarUsu(nome_aluno);
        if(usuario != null){
            TelaInicial janela = new TelaInicial();
            janela.setVisible(true);
            this.dispose();
            usuario.nome_logado = nome_aluno;
        }
        else{
            JOptionPane.showMessageDialog(null, "Usuario n√£o existe !");
        }
        
    }//GEN-LAST:event_JBLogarActionPerformed

