    private void JBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBCadastrarActionPerformed
        UsuarioDAO us = new UsuarioDAO();
        
        
        try {
            us.validarCadastro(JTFNome.getText());
        } catch (SQLException ex) {
            Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(Integer.parseInt(JTFIdade.getText())>= 1 && Integer.parseInt(JTFIdade.getText())<=120){
        if(!JTFNome.getText().equals("") && !JTFIdade.getText().equals("")){
            if(Usuario.valor == 0){
            usu.setNome(JTFNome.getText().toString());
            usu.setIdade(Integer.parseInt(JTFIdade.getText()));
            UsuarioDAO.cadastroUsu(usu);
            } else {JOptionPane.showMessageDialog(null, "Usuario j치 Existe");}
        }else{
            JOptionPane.showMessageDialog(null, "H치 um campo v치zio!");
        }
      } else {JOptionPane.showMessageDialog(null, "Digite uma idade v치lida");}
    }//GEN-LAST:event_JBCadastrarActionPerformed

